# include "3-calc.h"

/**
 * op_add - addition function
 * @a: number
 * @b: number
 *
 * Return: a addition b
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtraction function
 * @a: number
 * @b: number
 *
 * Return: a subtraction b
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplication function
 * @a: number
 * @b: number
 *
 * Return: a multiplicated by b
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - division function
 * @a: number
 * @b: number
 *
 * Return: a divided b
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - modulo function
 * @a: number
 * @b: number
 *
 * Return: the modulo of a divided b
 */

int op_mod(int a, int b)
{
	return (a % b);
}
